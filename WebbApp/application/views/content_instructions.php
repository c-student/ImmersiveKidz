	<h1>Användarmanual</h1>
	<h2>Installera på ny dator</h2>
	<p>
		Det bildbehandlingsbibliotek som används är <a href="http://www.imagemagick.org/script/index.php"> ImageMagick </a>. Detta behöver vara 
		installerat på den server där inläsningen av bilderna ska ske. Se “Instruktioner för utvecklare” för
		information om hur du installerar ImageMagick på en ny dator. 
	</p>

	<h2>Att tänka på innan</h2>
	<p>Bilder som har klara färger och tydliga linjer ger ett bättre slutresultat. Informera om
		möjligt detta för lärarna i förväg så att de är medvetna om att det påverkar resultatet. 
		Försök att undvika att barnen skriver på framsidan av teckningen. Detta då programmet inte
		särskiljer text från teckning. Om det ändå finns text på bilderna bör denna tas bort manuellt
		innan bilderna läses in i programmet.  
	</p>
	<h2>Scanning</h2>
	<p>
		Scanna in önskade bilder till en mapp på datorn. Det gör inget om bilderna scannas in upp- och ner
		då detta går att korrigera i programmet i efterhand.
	</p>

	<h2>Webbapplikationen</h2>
	<p>1. Starta webbapplikationen från (här behövs webbadress)</p>
	<p>2. Tryck på knappen "Välj filer" och navigera till mappen där de inscannade bilderna sparades.</p>
	<p>3. Öppna mappen och markera alla bilder (Använd tangenterna Ctrl eller Shift för att markera flera bilder). Tryck "Öppna".</p>
	<p>4. Fyll i gruppens namn och dagens datum i fälten. Tryck sedan på knappen "Ladda upp".</p>
	<p>5. Bilderna visas nu frilagda. För att tydligare se om friläggningen lyckades kan bilderna visas i större format
		genom att man klickar på bilden. Om friläggningen inte ser bra ut kan friläggningen anpassas genom att man drar
		i reglaget som visas under varje bild.</p>
	<p>6. Under varje bild finns fält för information. Fyll i informationen om vem som har ritat bilden, bildens namn
		och vilken typ av objekt bilden föreställer (ex. fisk, fågel, landdjur). Det finns även ett fält där det går att
		att fylla i en anteckning eller historia om bilden.(valfritt)</p>
	<p>7. (Valfritt) Det finns möjlighet att associera varje bild med ett ljud som spelas upp när man närmar sig bilden
		i domapplikationen. Välj "Ladda upp ljud" för att associera en bild med en ljudfil som finns på datorn.</p>
	<p>8. Tryck på "Gå vidare".</p>
	<p>9. Tryck på "Ladda ner" för att spara filerna i en zip-fil på hårddisken. Zip-filen sparas i den mapp som är angiven
	för filer nedladdade från internet.</p>
	<p>10. Sätt i en USB-sticka i datorn och för över zip-filen till USB-stickan</p>

